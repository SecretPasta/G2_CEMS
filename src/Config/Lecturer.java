package Config;

public class Lecturer {
    private String id; // ID of the lecturer
    private String username; // Username of the lecturer
    private String password; // Password of the lecturer
    private String name; // Name of the lecturer
    private String email; // Email of the lecturer

    // Constructor to initialize the Lecturer object with attribute values
    public Lecturer(String id, String username, String password, String name, String email) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.name = name;
        this.email = email;
    }

    // Getters and Setters to access and modify the attribute values

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
